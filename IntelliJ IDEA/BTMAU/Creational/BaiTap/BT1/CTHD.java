package Creational.BaiTap.BT1;

public class CTHD {
    private String sp;
    private int sl, dongia, chietkhau;
    public CTHD(String sp, int sl, int dongia, int chietkhau) {
        this.sp = sp;
        this.sl = sl;
        this.dongia = dongia;
        this.chietkhau = chietkhau;
    }
    public String getSp() {
        return sp;
    }
    public void setSp(String sp) {
        this.sp = sp;
    }
    public int getSl() {
        return sl;
    }
    public void setSl(int sl) {
        this.sl = sl;
    }
    public int getDongia() {
        return dongia;
    }
    public void setDongia(int dongia) {
        this.dongia = dongia;
    }
    public int getChietkhau() {
        return chietkhau;
    }
    public void setChietkhau(int chietkhau) {
        this.chietkhau = chietkhau;
    }
    
    
}
